//switch conditional are same all language

let difficulty = 4;

switch(difficulty){
    case 1:
        alert("Easy");
        break;
    case 2: 
        alert("Normal");
        break;
    case 3: 
        alert("Hard");
        break;
    default:
        alert("Invalid");
        break;
}

