//Comparison Equality

/*the difference of == and ===
the == it accept the value either it is numbers or string as long they are same
and the === they are same with == but it is strictly when it comes to datatypes*/

let x = 12;
let y = "12";

alert(x == 5);
alert(x === y);

let xx = 5;
let yy = 5;

/*the not equal is alternative of equal
  lets say the 5 is equal to 5 it means its true
  then in != it will make it false*/
alert(xx != yy);

//same with not equal but they are strict when it comes to datatypes
alert(xx !== yy);

//Comparison operators

alert(xx <= yy);

alert(xx > yy);

alert(xx >= yy);

alert(xx < yy);